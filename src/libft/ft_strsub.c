#include "libft.h"

char	*ft_strsub(char const *s, unsigned int start, size_t len)
{
	char	*res;

	res = malloc(sizeof(char) * len + 1);
	if (!res && !*s && !len)
		return (0);
	ft_strncpy(res, &s[start], len);
	return (res);
}
